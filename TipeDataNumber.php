<?php

/*Tipe Data Integer*/

echo "Decimal : ";
var_dump(1234);

echo "Octal : ";
var_dump(0123);

echo "Hexadecimal : ";
var_dump(0x1A);

echo "Binary : ";
var_dump(0b11111111);

echo "Underscore in number : ";
var_dump(14_582_728);

/*Tipe Data Float*/

echo "Floating Point : ";
var_dump(1.234);

echo "Floating Point dengan E notation Plus : (1.2 x 1000) : ";
var_dump(1.2e3);

echo "Floating Point dengan E notation Minus : (7 x 0.001): ";
var_dump(1.7e-3);

echo "Underscore di Floating Point : ";
var_dump(1_234.567);

echo "Integer Overflow : ";
var_dump(9223372036854775808);